package Day22;

public class Student {

    private String Name;
    private String City;

    public Student(String name, String city) {
        Name = name;
        City = city;
    }


    public String getName() {
        return Name;
    }

    public String getCity() {
        return City;
    }

    public void setName(String name) {
        Name = name;
    }

    public void setCity(String city) {
        City = city;
    }




}
